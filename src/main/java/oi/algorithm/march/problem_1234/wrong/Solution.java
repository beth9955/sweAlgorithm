class Solution
{
	public static void main(String args[]) throws Exception
	{

		//System.setIn(new FileInputStream("res/input.txt"));

		/*
		   표준입력 System.in 으로부터 스캐너를 만들어 데이터를 읽어옵니다.
		 */
		Scanner sc = new Scanner(System.in);
		int T=10;
		//T=sc.nextInt();
		/*
		   여러 개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
		*/

		for(int test_case = 1; test_case <= T; test_case++)
		{

			/////////////////////////////////////////////////////////////////////////////////////////////
			/*
				 이 부분에 여러분의 알고리즘 구현이 들어갑니다.
			 */
			/////////////////////////////////////////////////////////////////////////////////////////////
			Map<Character, Character> password = new TreeMap();
            int passwordLength = sc.nextInt();
            List<String> result = new ArrayList<>();
            String text = sc.next();
            for (int i=0; i<text.length(); i++){
                char number = text.charAt(i);
                if (!password.containsKey(number)){
                    result.add(String.valueOf(number));
                }
                password.put(number, number);
            }
           System.out.println("#" + test_case + " " + String.join("", result));
		}
	}
}